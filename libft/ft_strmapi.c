#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char			*str;
	unsigned int	i;
	unsigned int	l;

	i = 0;
	if (!s || !f)
	{
		return (NULL);
	}
	l = ft_strlen(s);
	str = (char *)malloc(sizeof(*s) * (l + 1));
	if (!str)
		return (NULL);
	while (s[i])
	{
		str[i] = (*f)(i, s[i]);
		i++;
	}
	str[i] = '\0';
	return (str);
}
